// sort() methid sorts an array alphabetically
const fruits = ["Banana", "Apple", "chary", "orange"]
console.log(fruits)
fruits.sort()
console.log(fruits)

// reverse() method reverse the elements in an arry 
fruits.reverse();
console.log(fruits)

